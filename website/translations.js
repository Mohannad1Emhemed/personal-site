let currentLang = 'en';
let typedInstance = null;

function changeLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    
    if (lang === 'ar') {
        document.body.classList.add('rtl');
    } else {
        document.body.classList.remove('rtl');
    }
    
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update job descriptions
    if (typeof updateJobDescriptions === 'function') {
        updateJobDescriptions(lang);
    }
    
    // Update project cards
    if (typeof updateProjectCards === 'function') {
        updateProjectCards(lang);
    }
    
    // Update timeline dates for RTL
    document.querySelectorAll('.timeline-date').forEach(dateEl => {
        const start = dateEl.getAttribute('data-date-start');
        const end = dateEl.getAttribute('data-date-end');
        
        if (start && end) {
            const presentText = translations[lang]['experience.present'];
            
            if (lang === 'ar') {
                // RTL: start - end (displayed with LTR to prevent reversal)
                dateEl.style.direction = 'ltr';
                dateEl.style.textAlign = 'left';
                if (end === 'present') {
                    dateEl.innerHTML = start + ' – ' + presentText;
                } else {
                    dateEl.textContent = start + ' – ' + end;
                }
            } else {
                // LTR: start - end
                dateEl.style.direction = '';
                dateEl.style.textAlign = '';
                if (end === 'present') {
                    dateEl.innerHTML = start + ' – <span data-i18n="experience.present">' + presentText + '</span>';
                } else {
                    dateEl.textContent = start + ' – ' + end;
                }
            }
        }
    });
    
    if (typedInstance) {
        typedInstance.destroy();
    }
    typedInstance = new Typed('#typed', {
        strings: translations[lang]['hero.typed'],
        typeSpeed: 50,
        backSpeed: 30,
        backDelay: 2000,
        loop: true,
        showCursor: true,
        cursorChar: '|'
    });
    
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    
    localStorage.setItem('preferredLanguage', lang);
}

document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    changeLanguage(savedLang);
    
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => changeLanguage(btn.dataset.lang));
    });
});
